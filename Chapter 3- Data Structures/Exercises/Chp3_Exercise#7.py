Locations = ['Japan' , 'Switzerland' , 'New Zealand' , 'Singapore']

# Original Order...
print ("Original Order:")
print (Locations)

# Alphabetical Order...
print ("\nAlphabetical Order:")
print (sorted(Locations))

print ("\nOriginal Order:")
print (Locations)

# Reverse Alphabetical Order...
print("\nReversed Alphabetical Order:")
print (sorted(Locations, reverse=True))

print ("\nOriginal Order:")
print (Locations)

# Reverse Order...

print("\nReversed Order")
Locations.reverse()
print(Locations)

print ("\nOriginal Order:")
print (Locations)

# Alphabitcal and Reversed Alphabetical again...

print ("\nAlphabetical Order:")
print (sorted(Locations))

print("\nReversed Alphabetical Order:")
print (sorted(Locations, reverse=True))
